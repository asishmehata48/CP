let name = "Asish";
let age = 21;

// Normal String Concatenation
console.log("Hello, my name is " + name + " and I am " + age + " years old.");

// Using Template Literals
console.log(`Hello, my name is ${name} and I am ${age} years old.`);

// Multi-line String
let msg = `
This is a multi-line
string using template literals.
`;
console.log(msg);
